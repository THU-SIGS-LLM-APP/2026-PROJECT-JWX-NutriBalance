export const getRecipeById = (id: string): Recipe | undefined => {
  return recipes.find(recipe => recipe.id === id);
};

export const getRecipesByMealType = (mealType: string): Recipe[] => {
  return recipes.filter(recipe => recipe.mealType.includes(mealType as any));
};

export const getRecipesByCuisine = (cuisine: string): Recipe[] => {
  return recipes.filter(recipe => recipe.cuisine === cuisine);
};

export const getQuickRecipes = (maxTime: number = 15): Recipe[] => {
  return recipes.filter(recipe => recipe.prepTime <= maxTime);
};
